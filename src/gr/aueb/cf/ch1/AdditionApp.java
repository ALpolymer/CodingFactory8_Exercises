/**
 * A simple app that initializes two integer variables and displays their sum
 */

public class AdditionApp {
    public static void main(String[] args) {

        int num1 = 23;
        int num2 = 34;
        int sum = 0;

        sum = num1 + num2;

        System.out.println("Το αποτέλεσμα της πρόσθεσης είναι ίσο με:" + sum);
    }
}
